    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String keyword = txtCari.getText(); // Ambil kata kunci dari TextField
        ResepController controller = new ResepController();
        List<Resep> hasilPencarian = controller.cariResep(keyword);

        // Tampilkan hasil pencarian di JList
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Resep resep : hasilPencarian) {
            model.addElement(resep.getJudul());
        }
        listJudul.setModel(model);

        // Jika tidak ada hasil
        if (hasilPencarian.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tidak ada resep yang cocok dengan pencarian Anda.");
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        ResepController controller = new ResepController();
        List<Resep> resepList = controller.getSemuaResep();

        // Pilih lokasi untuk menyimpan file
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan File Teks");
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Text Files", "txt"));
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            if (!filePath.endsWith(".txt")) {
                filePath += ".txt"; // Tambahkan ekstensi jika belum ada
            }

            try (PrintWriter writer = new PrintWriter(filePath)) {
                // Tulis data ke file teks
                for (Resep resep : resepList) {
                    writer.println("ID: " + resep.getId());
                    writer.println("Judul: " + resep.getJudul());
                    writer.println("Bahan: " + resep.getBahan());
                    writer.println("Langkah: " + resep.getLangkah());
                    writer.println("--------------------");
                }
                JOptionPane.showMessageDialog(this, "Data berhasil diekspor ke: " + filePath);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnExportActionPerformed

