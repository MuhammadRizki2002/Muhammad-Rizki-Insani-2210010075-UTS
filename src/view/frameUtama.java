    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String judul = txtJudul.getText();
        String bahan = txtBahan.getText();
        String langkah = txtLangkah.getText();

        if (judul.isEmpty() || bahan.isEmpty() || langkah.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!");
            return;
        }

        ResepController controller = new ResepController();
        Resep resep = new Resep(0, judul, bahan, langkah);
        controller.tambahResep(resep);

        JOptionPane.showMessageDialog(this, "Resep berhasil ditambahkan!");
        loadJList();
        clearForm();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedIndex = listJudul.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Pilih resep yang ingin diedit!");
            return;
        }

        String judul = txtJudul.getText();
        String bahan = txtBahan.getText();
        String langkah = txtLangkah.getText();

        if (judul.isEmpty() || bahan.isEmpty() || langkah.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi untuk mengedit!");
            return;
        }

        ResepController controller = new ResepController();
        List<Resep> resepList = controller.getSemuaResep();
        int id = resepList.get(selectedIndex).getId();

        Resep resep = new Resep(id, judul, bahan, langkah);
        controller.updateResep(resep);

        JOptionPane.showMessageDialog(this, "Resep berhasil diperbarui!");
        loadJList();
        clearForm();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedIndex = listJudul.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Pilih resep yang ingin dihapus!");
            return;
        }

        ResepController controller = new ResepController();
        List<Resep> resepList = controller.getSemuaResep();
        int id = resepList.get(selectedIndex).getId();

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus resep ini?");
        if (confirm == JOptionPane.YES_OPTION) {
            controller.hapusResep(id);
            JOptionPane.showMessageDialog(this, "Resep berhasil dihapus!");
            loadJList();
            clearForm();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

