    private void listJudulValueChanged(javax.swing.event.ListSelectionEvent evt) {
        // Abaikan jika tidak ada item yang dipilih
        if (listJudul.getSelectedIndex() == -1) {
            return;
        }

        // Dapatkan indeks item yang dipilih
        int selectedIndex = listJudul.getSelectedIndex();

        // Ambil data dari controller berdasarkan indeks
        ResepController controller = new ResepController();
        List<Resep> resepList = controller.getSemuaResep();

        if (selectedIndex < resepList.size()) {
            // Ambil resep berdasarkan indeks
            Resep selectedResep = resepList.get(selectedIndex);

            // Isi field dengan data resep yang dipilih
            txtJudul.setText(selectedResep.getJudul());
            txtBahan.setText(selectedResep.getBahan());
            txtLangkah.setText(selectedResep.getLangkah());
        }
    }

    private void loadJList() {
        ResepController controller = new ResepController();
        List<Resep> resepList = controller.getSemuaResep();

        DefaultListModel<String> model = new DefaultListModel<>();
        for (Resep resep : resepList) {
            model.addElement(resep.getJudul());
        }
        listJudul.setModel(model);
    }

    private void clearForm() {
        txtJudul.setText("");
        txtBahan.setText("");
        txtLangkah.setText("");
    }

